package com.qprogramming.tasq.manage;

public class Color {

	private Integer r;
	private Integer g;
	private Integer b;

	public Color(Integer r, Integer g, Integer b) {
		this.r = r;
		this.g = g;
		this.b = b;
	}

	public Integer getR() {
		return r;
	}

	public void setR(Integer r) {
		this.r = r;
	}

	public Integer getG() {
		return g;
	}

	public void setG(Integer g) {
		this.g = g;
	}

	public Integer getB() {
		return b;
	}

	public void setB(Integer b) {
		this.b = b;
	}

}
